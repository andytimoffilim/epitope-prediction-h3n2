#!/usr/bin/env python3
"""
Step 2: Extract global embeddings using ESM‑2 for all structures.

Purpose:
    Extract 1280‑dimensional protein sequence embeddings from ESM‑2 model.
    The embeddings are saved for later use in ensemble models.

Input:
    - h3n2_epitope_dataset.csv (generated by step 1)
    - .pdb files (downloaded by step 1)

Output:
    - new_ha_embeddings_enhanced.pkl – dictionary {pdb_id: embedding}

Dependencies:
    - fair-esm, torch, pandas, numpy
"""

import os
import pickle
import torch
import esm
import pandas as pd
import numpy as np

# Dictionary to convert three-letter amino acid codes to one-letter codes
AA3_TO_1 = {
    'ALA': 'A', 'ARG': 'R', 'ASN': 'N', 'ASP': 'D', 'CYS': 'C',
    'GLU': 'E', 'GLN': 'Q', 'GLY': 'G', 'HIS': 'H', 'ILE': 'I',
    'LEU': 'L', 'LYS': 'K', 'MET': 'M', 'PHE': 'F', 'PRO': 'P',
    'SER': 'S', 'THR': 'T', 'TRP': 'W', 'TYR': 'Y', 'VAL': 'V'
}

def extract_sequence_from_pdb(pdb_file, chain_id='A'):
    """Extract one-letter sequence from the specified chain of a PDB file."""
    seq = []
    with open(pdb_file) as f:
        for line in f:
            if line.startswith("ATOM") and line[21] == chain_id and line[12:16].strip() == "CA":
                resname = line[17:20].strip()
                aa = AA3_TO_1.get(resname, 'X')
                seq.append(aa)
    return ''.join(seq)

def get_embeddings_for_pdbs(pdb_ids, model, batch_converter, device='cpu'):
    """Return a dictionary {pdb_id: embedding} for the given list of PDB IDs."""
    embeddings = {}
    for pdb_id in pdb_ids:
        pdb_file = f"{pdb_id}.pdb"
        if not os.path.exists(pdb_file):
            print(f"File {pdb_file} not found, skipping.")
            continue
        try:
            # Special handling for 6WXY: chain B
            chain = 'B' if pdb_id == '6WXY' else 'A'
            seq = extract_sequence_from_pdb(pdb_file, chain_id=chain)
            if len(seq) == 0:
                print(f"Empty sequence for {pdb_file}, skipping.")
                continue
            # Prepare data for the model
            data = [(pdb_id, seq)]
            batch_labels, batch_strs, batch_tokens = batch_converter(data)
            batch_tokens = batch_tokens.to(device)
            with torch.no_grad():
                results = model(batch_tokens, repr_layers=[model.num_layers])
                token_embeddings = results["representations"][model.num_layers]  # (batch, tokens, dim)
                # Average over length, ignoring special start/end tokens
                embedding = token_embeddings[0, 1: len(seq)+1].mean(0).cpu().numpy()
            embeddings[pdb_id] = embedding
            print(f"Processed {pdb_id}, length {len(seq)}")
        except Exception as e:
            print(f"Error processing {pdb_id}: {e}")
    return embeddings

def main():
    # Load dataset to get list of PDB IDs
    df = pd.read_csv("h3n2_epitope_dataset.csv")
    pdb_ids = df['pdb_id'].unique()
    print(f"Total structures: {len(pdb_ids)}")

    # Device (GPU if available)
    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    print(f"Using device: {device}")

    # Load ESM-2 model (650M parameters)
    print("Loading ESM-2 model...")
    model, alphabet = esm.pretrained.esm2_t33_650M_UR50D()
    model = model.to(device)
    model.eval()
    batch_converter = alphabet.get_batch_converter()
    print("Model loaded.")

    # Extract embeddings
    embeddings = get_embeddings_for_pdbs(pdb_ids, model, batch_converter, device)

    # Save
    with open('new_ha_embeddings_enhanced.pkl', 'wb') as f:
        pickle.dump(embeddings, f)
    print(f"Embeddings saved for {len(embeddings)} structures.")

if __name__ == "__main__":
    main()