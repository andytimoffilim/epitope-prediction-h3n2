#!/usr/bin/env python3
"""
Step 3: Baseline ensemble model (MLP + Random Forest) for epitope prediction.
Now includes additional topological features: clustering, avg_neighbor_degree, coreness.

Purpose:
    Train an ensemble of MLP (with SMOTE and scaling) and Random Forest (with class_weight)
    on local features only. Perform Leave‑One‑Structure‑Out cross‑validation and
    output ROC‑AUC, PR‑AUC, and F1 scores.

Input:
    - h3n2_epitope_dataset.csv (generated by step 1, now includes new features)

Output:
    - Console output with cross‑validation metrics.
    - mlp_rf_ensemble_results.csv – detailed per‑fold results.

Dependencies:
    - pandas, numpy, scikit‑learn, imbalanced‑learn
"""

import pandas as pd
import numpy as np
from sklearn.neural_network import MLPClassifier
from sklearn.ensemble import RandomForestClassifier
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import roc_auc_score, average_precision_score, f1_score
from sklearn.model_selection import LeaveOneGroupOut
from imblearn.over_sampling import SMOTE
import warnings
warnings.filterwarnings("ignore")

# ========================
# 1. Load data
# ========================
df = pd.read_csv("h3n2_epitope_dataset.csv")

# Features: base structural/evolutionary + new topological features
feature_cols = [
    "degree",
    "clustering",
    "avg_neighbor_degree",
    "coreness",
    "surface_score",
    "hydro_score",
    "approx_sasa",
  #  "conservation",
] #+ [f"aa_oh_{i}" for i in range(20)]

# One‑hot encoding for region labels
region_dummies = pd.get_dummies(df["region_label"], prefix="region")
X = pd.concat([df[feature_cols], region_dummies], axis=1).values
y = df["epitope_label"].values
groups = df["pdb_id"].values  # grouping by structure

# ========================
# 2. Cross‑validation (LOSO)
# ========================
logo = LeaveOneGroupOut()
f1_scores = []
roc_auc_scores = []
pr_auc_scores = []
results = []  # detailed per‑fold results

print("Starting Leave‑One‑Structure‑Out cross‑validation (baseline MLP+RF)")
print(f"Total structures: {len(np.unique(groups))}\n")

for fold, (train_idx, test_idx) in enumerate(logo.split(X, y, groups)):
    test_pdb = df.iloc[test_idx]["pdb_id"].iloc[0]
    print(f"\n=== Fold {fold+1}: test on {test_pdb} ===")

    X_train, X_test = X[train_idx], X[test_idx]
    y_train, y_test = y[train_idx], y[test_idx]

    # Skip folds where test set has no epitopes (metrics undefined)
    if np.sum(y_test) == 0:
        print("   No epitopes in test, skipping.")
        continue

    # Normalization (only for MLP)
    scaler = StandardScaler()
    X_train_scaled = scaler.fit_transform(X_train)
    X_test_scaled = scaler.transform(X_test)

    # SMOTE for MLP
    smote = SMOTE(random_state=154, k_neighbors=6)
    X_train_res, y_train_res = smote.fit_resample(X_train_scaled, y_train)

    # --- MLP ---
    mlp = MLPClassifier(
        hidden_layer_sizes=(256, 128,64),
        max_iter=500,
        random_state=623,
        early_stopping=True,
        validation_fraction=0.1,
        verbose=False,
        alpha=0.001
    )
    mlp.fit(X_train_res, y_train_res)
    y_prob_mlp = mlp.predict_proba(X_test_scaled)[:, 1]

    # --- Random Forest (no scaling, class_weight='balanced') ---
    rf = RandomForestClassifier(
        n_estimators=100,
        class_weight='balanced',
        random_state=22,
        n_jobs=-1
    )
    rf.fit(X_train, y_train)
    y_prob_rf = rf.predict_proba(X_test)[:, 1]

    # Ensemble: average probabilities
    y_prob_ensemble = (y_prob_mlp + y_prob_rf) / 2
    y_pred_ensemble = (y_prob_ensemble > 0.5).astype(int)

    # Metrics
    roc_auc = roc_auc_score(y_test, y_prob_ensemble)
    pr_auc = average_precision_score(y_test, y_prob_ensemble)
    f1 = f1_score(y_test, y_pred_ensemble)

    f1_scores.append(f1)
    roc_auc_scores.append(roc_auc)
    pr_auc_scores.append(pr_auc)

    print(f"   ROC‑AUC:      {roc_auc:.3f}")
    print(f"   PR‑AUC:       {pr_auc:.3f}")
    print(f"   F1 (0.5):     {f1:.3f}")

    results.append({
        "test_pdb": test_pdb,
        "roc_auc": roc_auc,
        "pr_auc": pr_auc,
        "f1": f1,
        "n_epitopes_test": np.sum(y_test),
    })

# ========================
# 3. Summary
# ========================
print("\n" + "=" * 50)
print("CROSS‑VALIDATION RESULTS (BASELINE MLP+RF)")
print("=" * 50)
print(f"Number of folds with epitopes: {len(f1_scores)}")
print(f"ROC‑AUC:    {np.mean(roc_auc_scores):.3f} ± {np.std(roc_auc_scores):.3f}")
print(f"PR‑AUC:     {np.mean(pr_auc_scores):.3f} ± {np.std(pr_auc_scores):.3f}")
print(f"F1 (0.5):   {np.mean(f1_scores):.3f} ± {np.std(f1_scores):.3f}")

# Save detailed results
results_df = pd.DataFrame(results)
results_df.to_csv("mlp_rf_ensemble_results.csv", index=False)
print("\nDetailed results saved to mlp_rf_ensemble_results.csv")